package com.operators;

import java.util.Scanner;

public class Arithmatic_operator {
	public static void main(String[] args) {
		Scanner scanner=new Scanner(System.in);
		System.out.println("Addition");
		System.out.println("Enter value A");
		int a = scanner.nextInt();
		System.out.println("Enter value B");
		int b = scanner.nextInt();
		System.out.println("your Addition Answer is :" +(a+b));
		
		
		System.out.println("Substraction");
		System.out.println("Enter value A");
		int c = scanner.nextInt();
		System.out.println("Enter value B");
		int d = scanner.nextInt();
		System.out.println("your Substraction Answer is :" +(c-d));
		
		System.out.println("Multiplication");
		System.out.println("Enter value A");
		int e = scanner.nextInt();
		System.out.println("Enter value B");
		int f = scanner.nextInt();
		System.out.println("your Multiplication Answer is :" +(e*f));
		
		System.out.println("Division");
		System.out.println("Enter value A");
		int g = scanner.nextInt();
		System.out.println("Enter value B");
		int h = scanner.nextInt();
		System.out.println("your Division Answer is :" +(g/h));
		
		System.out.println("Modulus");
		System.out.println("Enter value A");
		int i = scanner.nextInt();
		System.out.println("Enter value B");
		int j = scanner.nextInt();
		System.out.println("your Modulus Answer is :" +(i%j));
		
		
		
		
		
		
		
	}

}

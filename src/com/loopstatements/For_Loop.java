package com.loopstatements;

public class For_Loop {
	public static void main(String[] args) {
		
		
		
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		
	}

}


       /*for loop syntax; for(initialization;condition;updation);
         
               initialization = starting point
               condition:
               updation = i++  or i--  
                     i=i+10
                     i=i-10
              */
                
package com.loopstatements;

public class While_Loop {
public static void main(String[] args) {
	int i=10;
	while (i>=1) {
		System.out.println(i);
		i--;
	}
}
}


            /* while loop syntax :
                             while(condition){
                                system.out.println();
                                increment (or) decrement:
                                }
             */


              /*
                 i>10;  ==>  i--;
                 i>=10; ==>  i--;
                 i<10;  ==>   i++;
                 i<=10; ==>  i++;
        */
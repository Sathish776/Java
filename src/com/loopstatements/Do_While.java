package com.loopstatements;

public class Do_While {
public static void main(String[] args) {
	int i=20;
	do {
		System.out.println(i);
		i++;
	} while (i<=50); 
}
}




            /* Do while syntax:
             
                       do{
                          system.out.println();
                          increment (or) decrement;
                          }
                          while (condition);
          */